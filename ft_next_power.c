unsigned int	next_power(unsigned int nb)
{
	if (!nb)
		return (2);
	if ((nb & (nb - 1)) == 0)
		nb <<= 1;
	else
	{
		nb--;
		nb |= nb >> 1;
		nb |= nb >> 2;
		nb |= nb >> 4;
		nb |= nb >> 8;
		nb |= nb >> 16;
		nb++;
	}
	return (nb);
}
